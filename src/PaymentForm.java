    public PaymentForm() {
        initComponents();
        memberIdSelect.addItem("Select Member");
        members.add(null);
        try (Connection conn = DbConfig.getConnection()) {
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("select * from info");
            if (rs.next()) {
                Member member = new Member();
                member.name = rs.getString("NAME");
                member.mobileNumber = rs.getString("MOBILENUMBER");
                members.add(member);
                memberIdSelect.addItem(member.name + " (" + member.mobileNumber + ")");
            }
            DbConfig.closeStatement(stmt, rs);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        try (Connection conn = DbConfig.getConnection()) {
              
            Statement statement = conn.createStatement();
            ResultSet result = statement.executeQuery("select * from payment");
            if (result.next()) {
                Payment payment = new Payment();
                payment.id = result.getInt("id");
                payment.date = result.getString("payment_date");
                payment.member = result.getString("member");
                payment.amount = result.getString("amount");
                DefaultTableModel tm = (DefaultTableModel) paymentTbl.getModel();
                tm.addRow(new Object[]{payment.date, payment.amount});
            }
            DbConfig.closeStatement(statement, result);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

