    private void memberIdSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_memberIdSelectActionPerformed
        if (memberIdSelect.getSelectedIndex() > 0) {
            selectedMember = members.get(memberIdSelect.getSelectedIndex());
            memberNameTxt.setText(selectedMember.name);
            mobileTxt.setText(selectedMember.mobileNumber);
            paymentAmountTxt.setText(selectedMember.amount);
        } else {
            selectedMember = null;
            memberNameTxt.setText("");
            mobileTxt.setText("");
            paymentAmountTxt.setText("0");
        }
    }//GEN-LAST:event_memberIdSelectActionPerformed

